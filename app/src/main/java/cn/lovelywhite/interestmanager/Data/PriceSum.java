package cn.lovelywhite.interestmanager.Data;

public class PriceSum {
    private String interestId;
    private String interestName;
    private Double interestPriceSum;

    public String getInterestId() {
        return interestId;
    }

    public void setInterestId(String interestId) {
        this.interestId = interestId;
    }

    public String getInterestName() {
        return interestName;
    }

    public void setInterestName(String interestName) {
        this.interestName = interestName;
    }

    public Double getInterestPriceSum() {
        return interestPriceSum;
    }

    public void setInterestPriceSum(Double interestPriceSum) {
        this.interestPriceSum = interestPriceSum;
    }
}
